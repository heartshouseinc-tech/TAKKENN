/**
 * 指定年度の問題を自動収集してFirestoreに保存
 * @param {number} year
 * @param {number[]|null} nos - null で1〜50
 * @param {function} onProgress - (step, current, total, message)
 */
async function tkAutoCollect(year, nos = null, onProgress = () => {}) {
  const fb = _fb();
  const targets = nos || Array.from({ length: 50 }, (_, i) => i + 1);
  const logId = year + '_' + Date.now();
  const saved = [], errors = [];

  await fb.setDoc(fb.doc(fb.db, COL_LOG, logId), {
    year, targets, status: 'running', startedAt: fb.serverTimestamp(), saved: 0, errors: []
  });

  for (let i = 0; i < targets.length; i++) {
    const no = targets[i];
    onProgress('collecting', i + 1, targets.length, year + '年 問' + no + ' を収集中...');
    try {
      // 既存チェック
      const exists = await tkGetQuestion(year, no);
      if (exists) {
        onProgress('skip', i + 1, targets.length, year + '年 問' + no + ' はすでに保存済み');
        saved.push({ no, status: 'skipped' });
        continue;
      }
      const q = await window.TakkenSearch.analyzeURL(
        'https://takken-siken.com/kakomon/' + year + '/' + String(no).padStart(2, '0') + '.html',
        year, no
      );
      if (q) {
        q.source = 'auto_collected';
        await tkSaveQuestion(q);
        saved.push({ no, status: 'saved', category: q.category });
        onProgress('saved', i + 1, targets.length, '✅ ' + year + '年 問' + no + '（' + q.category + '）保存完了');
      } else {
        errors.push({ no, error: '問題解析失敗' });
        onProgress('error', i + 1, targets.length, '⚠️ ' + year + '年 問' + no + ' 解析失敗');
      }
      await new Promise(r => setTimeout(r, 1000));
    } catch(e) {
      errors.push({ no, error: e.message });
      onProgress('error', i + 1, targets.length, '❌ ' + year + '年 問' + no + ' エラー: ' + e.message);
      await new Promise(r => setTimeout(r, 500));
    }
  }

  await fb.setDoc(fb.doc(fb.db, COL_LOG, logId), {
    year, targets, status: 'done',
    startedAt: fb.serverTimestamp(), finishedAt: fb.serverTimestamp(),
    saved: saved.filter(s => s.status === 'saved').length,
    skipped: saved.filter(s => s.status === 'skipped').length,
    errors
  });

  return {
    year, total: targets.length,
    saved:   saved.filter(s => s.status === 'saved').length,
    skipped: saved.filter(s => s.status === 'skipped').length,
    errors
  };
}

