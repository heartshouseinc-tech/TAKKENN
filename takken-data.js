/** 回答結果を保存 */
async function tkSaveAnswer(uid, year, no, correct) {
  const fb = _fb();
  const id = uid + '_' + year + '_' + String(no).padStart(2, '0') + '_' + Date.now();
  await fb.setDoc(fb.doc(fb.db, COL_STATS, id), {
    uid, year, no, correct, answeredAt: fb.serverTimestamp()
  });
}

/** ユーザーの学習統計取得 */
async function tkGetStats(uid) {
  const fb = _fb();
  const snap = await fb.getDocs(fb.collection(fb.db, COL_STATS));
  const records = snap.docs.map(d => d.data()).filter(r => r.uid === uid);
  const stats = { total: records.length, correct: records.filter(r => r.correct).length, byCategory: {}, weakPoints: [] };
  stats.accuracy = stats.total > 0 ? Math.round(stats.correct / stats.total * 100) : 0;
  records.forEach(r => {
    if (!stats.byCategory[r.category]) stats.byCategory[r.category] = { total: 0, correct: 0 };
    stats.byCategory[r.category].total++;
    if (r.correct) stats.byCategory[r.category].correct++;
  });
  Object.entries(stats.byCategory).forEach(([cat, s]) => {
    const acc = s.total > 0 ? Math.round(s.correct / s.total * 100) : 0;
    if (s.total >= 5 && acc < 60) stats.weakPoints.push({ category: cat, accuracy: acc });
  });
  stats.weakPoints.sort((a, b) => a.accuracy - b.accuracy);
  return stats;
}

