// ============================================================

/** 問題を保存（新規/上書き） */
async function tkSaveQuestion(q) {
  const fb = _fb();
  const id = q.year + '_' + String(q.no).padStart(2, '0');
  await fb.setDoc(fb.doc(fb.db, COL_Q, id), {
    ...q, id,
    source: q.source || 'ai_collected',
    updatedAt: fb.serverTimestamp()
  });
  return id;
}

/** 複数問題を一括保存 */
async function tkSaveQuestions(questions, onProgress = () => {}) {
  const res = { saved: 0, errors: [] };
  for (let i = 0; i < questions.length; i++) {
    try {
      await tkSaveQuestion(questions[i]);
      res.saved++;
    } catch(e) {
      res.errors.push({ index: i, error: e.message });
    }
    onProgress(i + 1, questions.length);
  }
  return res;
}

/** 問題を1件取得 */
async function tkGetQuestion(year, no) {
  const fb = _fb();
  const id = year + '_' + String(no).padStart(2, '0');
  const snap = await fb.getDoc(fb.doc(fb.db, COL_Q, id));
  return snap.exists() ? snap.data() : null;
}

/** 全問題取得（フィルター付き） */
async function tkGetAllQuestions(filter = {}) {
  const fb = _fb();
  const snap = await fb.getDocs(fb.collection(fb.db, COL_Q));
  let qs = snap.docs.map(d => d.data());
  if (filter.category) qs = qs.filter(q => q.category === filter.category);
  if (filter.year)     qs = qs.filter(q => q.year === filter.year);
  if (filter.source)   qs = qs.filter(q => q.source === filter.source);
  return qs.sort((a, b) => b.year - a.year || a.no - b.no);
}

/** 問題削除 */
async function tkDeleteQuestion(year, no) {
  const fb = _fb();
  const id = year + '_' + String(no).padStart(2, '0');
  await fb.deleteDoc(fb.doc(fb.db, COL_Q, id));
}

/** 問題数カウント（分野・年度別） */
async function tkCountQuestions() {
  const fb = _fb();
  const snap = await fb.getDocs(fb.collection(fb.db, COL_Q));
  const counts = { total: 0, byYear: {}, byCategory: {} };
  snap.docs.forEach(d => {
    const q = d.data(); counts.total++;
    counts.byYear[q.year] = (counts.byYear[q.year] || 0) + 1;
    counts.byCategory[q.category] = (counts.byCategory[q.category] || 0) + 1;
  });
  return counts;
}

